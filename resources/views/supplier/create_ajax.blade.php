<script>
    $(document).ready(function() {
        $("#form-tambah-supplier").validate({
            rules: {
                supplier_kode: {
                    required: true,
                    minlength: 3,
                    maxlength: 10
                },
                nama: {
                    required: true,
                    minlength: 3,
                    maxlength: 50
                },
                alamat: {
                    required: true,
                    minlength: 5,
                    maxlength: 255
                }
            },
            submitHandler: function(form) {
                $.ajax({
                    url: form.action,
                    type: form.method,
                    data: $(form).serialize(),
                    success: function(response) {
                        if (response.status) {
                            $('#modal-supplier').modal('hide');
                            Swal.fire({
                                icon: 'success',
                                title: 'Berhasil',
                                text: response.message
                            });
                            dataSupplier.ajax.reload(); // Refresh DataTable
                        } else {
                            $('.error-text').text('');
                            $.each(response.msgField, function(prefix, val) {
                                $('#error-' + prefix).text(val[0]);
                            });
                            Swal.fire({
                                icon: 'error',
                                title: 'Terjadi Kesalahan',
                                text: response.message
                            });
                        }
                    }
                });
                return false;
            }
        });
    });
</script>
